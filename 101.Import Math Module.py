import math

num = float(input("Enter a number: "))

sqrt_value = math.sqrt(num)
floor_value = math.floor(num)
ceiling_value = math.ceil(num)

print(f"The square root of {num} is {sqrt_value}")
print(f"The floor value of {num} is {floor_value}")
print(f"The ceiling value of {num} is {ceiling_value}")